        public async Task AddToRoleAsync(ApplicationUser user, string roleName, CancellationToken cancellationToken)
        {
            cancellationToken.ThrowIfCancellationRequested();

            using (var connection = new SqlConnection(_connectionString))
            {
                await connection.OpenAsync(cancellationToken);

                  var sqlRole ="SELECT ROLEID FROM ApplicationRole where Name = @Name";

                var returnedRoleID = await connection.QueryAsync<int>(sqlRole, new { Name = roleName });

                  var sql = " INSERT INTO [ApplicationUserRole] " +
                            "            ([UserID]				" +
                            "            ,[RoleID])				" +
                            "      VALUES						" +
                            "            (@UserID				" +
                            "            ,@RoleID)				";
                await connection.ExecuteAsync(sql, new { UserID = user.Id, RoleID = returnedRoleID });

            }
            
        }

